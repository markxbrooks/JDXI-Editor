def generate_preset_list():
    presets = []
    for line in RAW_PRESETS.split('\n'):
        if not line.strip():
            continue
        parts = line.split()
        preset_id = parts[0]
        name_parts = []
        i = 1
        while i < len(parts) and not parts[i] in ['Strings/Pad', 'Lead', 'Bass', 'Keyboard', 'FX/Other', 'Seq', 'Brass']:
            name_parts.append(parts[i])
            i += 1
        name = ' '.join(name_parts)
        category = parts[i]
        msb = parts[i + 1]
        lsb = parts[i + 2]
        pc = parts[i + 3]
        
        presets.append({
            "id": preset_id.zfill(3),
            "name": name,
            "category": category,
            "msb": msb,
            "lsb": lsb,
            "pc": pc
        })
    return presets


PRESET_LIST = generate_preset_list()
